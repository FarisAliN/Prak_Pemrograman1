public class CobaVar {
    public static void main(String [] args){
        String namaDepan = "Faris";
        String namaBelakang = "Ali Naufal";
        int umur = 18;
        double tinggiBadan = 170;
        double beratBadan = 68.0;

        System.out.println("Nama : " + namaDepan + " " + namaBelakang);
        System.out.println("Umur : " + umur + " tahun");
        System.out.println("Tinggi Badan : " + (int)tinggiBadan + " cm");
        System.out.println("Berat Badan : " + beratBadan + " kg");
    }
}